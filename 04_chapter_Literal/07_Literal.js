// Literals in JavaScript are fixed values that are directly written into the code. They can be of different types such as string, number, boolean, null, and undefined.

let str="Manish" // This is a string literal
let num=25 // This is a number literal
let bool=true // This is a boolean literal
let pi=3.14 // This is a numeric literal
let nullValue=null // This is a null literal
let undefinedValue;  // This is an undefined literal

// Numeric Literals can be represented in different formats:
 //1: int
 //2: float

 console.log(typeof str); // Output: string
 console.log(typeof num); // Output: number
 console.log(typeof bool); // Output: boolean
 console.log(typeof pi); // Output: number
 console.log(typeof nullValue); // Output: object
 console.log(typeof undefinedValue); // Output: undefined

 