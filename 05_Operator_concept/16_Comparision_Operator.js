// comparision operator in JS
//  ==, !=, ===, !==, <, >, <=, >=
// = -> Assignment operator

// == -> checks for equality of values, but not types
// eg -> 5 == "5" -> true

// === -> checks for equality of values and types
// eg -> 5 === "5" -> false


console.log(5 == "5"); // true loose comparison
console.log(5 === "5"); // false strict comparison


console.log(5 != "5"); // false loose comparison
console.log(5 !== "5"); // true strict comparison   
