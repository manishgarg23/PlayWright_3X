// Logical Operators in JavaScript

// Logical operators are used to combine or invert boolean values. They are commonly used in conditional statements and loops to control the flow of a program based on multiple conditions.

// 1. Logical AND (&&): Returns true if both operands are true, otherwise returns false.
let a = true;
let b = false;
console.log(a && b); // Output: false

// 2. Logical OR (||): Returns true if at least one of the operands is true, otherwise returns false.
console.log(a || b); // Output: true    

// 3. Logical NOT (!): Inverts the boolean value of the operand. If the operand is true, it returns false; if it's false, it returns true.
console.log(!a);

